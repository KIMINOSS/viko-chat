import { useLocation, useNavigate } from 'react-router-dom';

const navItems = [
  { path: '/', label: 'Chats', icon: ChatIcon },
  { path: '/contacts', label: 'Contacts', icon: ContactIcon },
  { path: '/settings', label: 'Settings', icon: SettingsIcon },
] as const;

export function BottomNav() {
  const { pathname } = useLocation();
  const navigate = useNavigate();

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 border-t border-gray-100 bg-white/95 pb-[var(--sab)] backdrop-blur-lg">
      <div className="mx-auto flex max-w-lg items-center justify-around">
        {navItems.map(({ path, label, icon: Icon }) => {
          const active = path === '/' ? pathname === '/' : pathname.startsWith(path);
          return (
            <button
              key={path}
              onClick={() => navigate(path)}
              className={`flex flex-1 flex-col items-center gap-0.5 py-2 text-[10px] font-medium transition ${
                active ? 'text-indigo-500' : 'text-gray-400'
              }`}
            >
              <Icon active={active} />
              {label}
            </button>
          );
        })}
      </div>
    </nav>
  );
}

